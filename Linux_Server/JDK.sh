# Passos para Instalar o JDK no Linux Mint

sudo add-apt-repository ppa:linuxuprising/java
sudo apt update


# Instalar o JDK (Por exemplo, OpenJDK 22 se disponível, ou a última versão):

# Primeiro, verifique a versão disponível:

sudo apt search openjdk

# Em seguida, instale a versão desejada (ajuste o número da versão se necessário):

sudo apt install openjdk-22-jdk

sudo apt install openjdk-21-jdk